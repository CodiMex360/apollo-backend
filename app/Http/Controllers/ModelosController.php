<?php

namespace App\Http\Controllers;

use App\Enums\JsonResponse;
use App\Models\Modelos;
use Illuminate\Http\Request;

class ModelosController extends Controller
{
  /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $modelos = Modelos::where('activo', true)->orderBy('id', 'DESC')->get();
        $modelos->load('marca');

        return response()->json([
            'ok' => true,
            'modelos' => $modelos
        ], JsonResponse::OK);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return response()->json([
            'ok' => false,
            'errors' => ['Not available']
        ], JsonResponse::BAD_REQUEST);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validateData = Modelos::validateBeforeSave($request->all());

        if ($validateData !== true) {
            return response()->json([
                'ok' => false,
                'errors' => $validateData
            ], JsonResponse::BAD_REQUEST);
        }

        $modelo = new Modelos();
        $modelo->modelo = $request->modelo;
        $modelo->marca_id = $request->marca_id;
        $modelo->activo = true;

        if ($modelo->save()) {
            return response()->json([
                'ok' => true,
                'message' => 'Modelo registrado correctamente'
            ], JsonResponse::OK);
        } else {
            return response()->json([
                'ok' => false,
                'errors' => ['Algo salio mal, intente nuevamente']
            ], JsonResponse::BAD_REQUEST);
        }
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $modelo = Modelos::where('id', $id)->first();
        $modelo->load('marca');

        if (!$modelo) {
            return response()->json([
                'ok' => false,
                'errors' => ['No se encontro la informaci칩n solicitada']
            ], JsonResponse::BAD_REQUEST);
        }

        return response()->json([
            'ok' => true,
            'modelo' => $modelo
        ], JsonResponse::OK);
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        return response()->json([
            'ok' => false,
            'errors' => ['Not available']
        ], JsonResponse::BAD_REQUEST);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $validateData = Modelos::validateBeforeSave($request->all(), true);

        if ($validateData !== true) {
            return response()->json([
                'ok' => false,
                'errors' => $validateData
            ], JsonResponse::BAD_REQUEST);
        }

        $modelo = Modelos::where('id', $id)->first();
        if (!$modelo) {
            return response()->json([
                'ok' => false,
                'errors' => ['No se encontro la informaci칩n solicitada']
            ], JsonResponse::BAD_REQUEST);
        }
        $modelo->modelo = $request->modelo;
        $modelo->marca_id = $request->marca_id;

        if ($modelo->save()) {
            return response()->json([
                'ok' => true,
                'message' => 'Modelo actualizado correctamente'
            ], JsonResponse::OK);
        } else {
            return response()->json([
                'ok' => false,
                'errors' => ['Algo salio mal, intente nuevamente']
            ], JsonResponse::BAD_REQUEST);
        }
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        if (!$id) {
            return response()->json([
                'ok' => false,
                'errors' => ['Proporcione un dato v치lido']
            ], JsonResponse::BAD_REQUEST);
        }

        $modelo = Modelos::where('id', $id)->first();

        if (!$modelo) {
            return response()->json([
                'ok' => false,
                'errors' => ['No se encontro la informaci칩n solicitada']
            ], JsonResponse::BAD_REQUEST);
        }

        $modelo->activo = false;

        if ($modelo->save()) {
            return response()->json([
                'ok' => true,
                'message' => 'Modelo dado de baja correctamente'
            ], JsonResponse::OK);
        } else {
            return response()->json([
                'ok' => false,
                'errors' => ['Algo salio mal, intente nuevamente']
            ], JsonResponse::BAD_REQUEST);
        }
    }

    public function getAll(Request $request) {
        $modelos = Modelos::orderBy('id', 'DESC')->get();
        $modelos->load('marca');

        return response()->json([
            'ok' => true,
            'modelos' => $modelos
        ], JsonResponse::OK);
    }

    public function enable($id) {
        $data = Modelos::where('id', $id)->first();
        if (!$data) {
            return response()->json([
                'ok' => false,
                'errors' => ['No hay registros']
            ], JsonResponse::BAD_REQUEST);
        }

        if ($data->activo === 1 || $data->activo == true) {
            return response()->json([
                'ok' => false,
                'errors' => ['El registro ya fue activado']
            ], JsonResponse::BAD_REQUEST);
        }

        $data->activo = true;

        if ($data->save()) {
            return response()->json([
                'ok' => true,
                'message' => 'Registro habilitado correctamente'
            ], JsonResponse::OK);
        }
    }
}
